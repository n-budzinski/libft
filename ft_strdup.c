#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	void	*ptr;

	len = ft_strlen(s);
	ptr = (char *)malloc(len + 1);
	if (ptr == NULL)
		return (NULL);
	ft_strlcpy(ptr, s, len + 1);
	return (ptr);
}
