#include "libft.h"

char	*strrchr(const char *s, int c)
{
	size_t i;
	char *t;

	i = 0;
	t = NULL;
	while(s[i] != '\0')
	{
		if(s[i] == c)
			t = (char *)(s + i);
		i++;
	}
	return (t);
}
