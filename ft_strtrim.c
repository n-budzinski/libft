#include "libft.h"
#include <stdio.h>

static int isinset(char c, char const *set)
{
	int i;

	i = 0;
	while (set[i] != '\0')
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char *ft_strtrim(char const *s1, char const *set)
{
	char *ptr;
	size_t i;
	size_t j;
	
	i = 0;
	j = ft_strlen(s1) - 1;
	while(isinset(s1[i], set))
	{
		i++;
	}
	while(isinset(s1[j], set))
	{
		j--;
	}
	ptr = malloc((j - i + 1));
	if(ptr != NULL)
		ft_strlcpy(ptr, s1 + i, j - i + 2);
	return (ptr);
}
