#include "libft.h"
#include <stdio.h>

static int	isinset(char c, char const *set)
{
	int	i;

	i = 0;
	while (set[i] != '\0')
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*ptr;
	size_t	i;
	size_t	j;
	size_t	s1_len;

	if (!s1 || !set)
		return (NULL);
	s1_len = ft_strlen(s1);
	i = 0;
	j = s1_len - 1;
	while (isinset(s1[i], set))
		i++;
	if (i == s1_len)
	{
		ptr = malloc(sizeof(char));
		if (ptr)
			*ptr = 0x00;
		return (ptr);
	}
	while (isinset(s1[j], set))
		j--;
	ptr = malloc((j - i + 2) * sizeof(char));
	if (ptr != NULL)
		ft_strlcpy(ptr, s1 + i, j - i + 2);
	return (ptr);
}
