#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	value;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	value = n % 10 + '0';
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		write(fd, &value, 1);
	}
	else
		write(fd, &value, 1);
}
