#include "libft.h"

static int	mul_overflow(size_t v1, size_t v2)
{
	if (2147483647 / v1 > v2)
		return (1);
	return (0);
}

void 	*ft_calloc(size_t nmemb, size_t size)
{
	size_t mem;
	void *ptr;

	if (mul_overflow(nmemb, size))
		return (NULL);
	mem = nmemb * size;
	ptr = malloc(nmemb * size);
	if (ptr != NULL)
		ft_bzero(ptr, mem);
	return (ptr);
}
