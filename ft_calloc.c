#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	mem;
	void	*ptr;

	mem = nmemb * size;
	if (mem && mem / nmemb != size)
		return (NULL);
	ptr = (void *)malloc(mem);
	if (ptr != NULL)
		ft_bzero(ptr, mem);
	return (ptr);
}
