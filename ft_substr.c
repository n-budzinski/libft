#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ptr;
	size_t	s_len;

	if (!s)
		return (0);
	if ((size_t)ft_strlen(s) <= start)
	{
		ptr = ft_strdup("");
		return (ptr);
	}
	s_len = ft_strlen(s + start);
	if (s_len > len)
		s_len = len;
	ptr = (char *)malloc(s_len + 1);
	if (!ptr)
		return (0);
	ft_strlcpy(ptr, s + start, s_len + 1);
	return (ptr);
}
