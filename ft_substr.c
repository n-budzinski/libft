#include "libft.h"

char *ft_substr(char const *s, unsigned int start, size_t len)
{
	void *ptr;
	size_t subsize;

	if (start >= (unsigned int)ft_strlen(s))
		return (ft_strdup(""));
	subsize = ft_strlen(s + start);
	if (len > subsize)
		len = subsize;
	ptr = ft_calloc(len + 1, sizeof(char));
	if (ptr != NULL)
		ft_strlcpy(ptr, s + start, subsize + 1);
	return (ptr);
}
